package com.example.eliminar.ChecklistJsonClass;

public class Checklist {
    private Esito esito;
    private PDA pda;
    private Info_step info_step;
    private Passo passo;




    //    Getters and Setters

    public Esito getEsito() {
        return esito;
    }
    public void setEsito(Esito esito) {
        this.esito = esito;
    }
    public PDA getPda() {
        return pda;
    }
    public void setPda(PDA pda) {
        this.pda = pda;
    }
    public Info_step getInfo_step() {
        return info_step;
    }
    public void setInfo_step(Info_step info_step) {
        this.info_step = info_step;
    }
    public Passo getPasso() {
        return passo;
    }
    public void setPasso(Passo passo) {
        this.passo = passo;
    }


}
